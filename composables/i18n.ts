  const supportedLocales = Object.keys(dict)

  function hasKey(key: string, l?: string) {
    const lang = l || locale.value
    const table = dict[lang] || {}
    return Object.prototype.hasOwnProperty.call(table, key)
  }

  function t(key: string, params?: Record<string, any>) {
    const base = dict[locale.value] || dict.en
    let s = base[key] || dict.en[key] || key
    if (params) {
      for (const k of Object.keys(params)) {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(params[k]))
      }
    }
    return s
  }

  function setLocale(l: string) {
    locale.value = l
    try { localStorage.setItem('locale', l) } catch {}
  }

  if (process.client) {
    onMounted(() => {
      try {
        const saved = localStorage.getItem('locale')
        if (saved) locale.value = saved
      } catch {}
    })
  }

  watch(() => locale.value, (l) => {
    try { localStorage.setItem('locale', String(l || 'fr')) } catch {}
  })

  return { locale, t, setLocale, supportedLocales, hasKey }
